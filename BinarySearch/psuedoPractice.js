function binarySearch(arr, n) {
  let left = 0;
  let right = arr.length - 1;

  while (left <= right) {
    let middle = Math.floor((left + right) / 2);
    let currElem = arr[middle];

    if (currElem === n) {
      return arr.indexOf(currElem);
    } else if (currElem < n) {
      left = ++middle;
    } else if (currElem > n) {
      right = ++middle;
    }
  }
  return -1;
}

